
-- 1) Órfãos numéricos (sem parent_sku ou parent_sku sem master, e a chave é totalmente numérica)
WITH orphans AS (
  SELECT id
  FROM pos_products
  WHERE (parent_sku IS NULL OR parent_sku = '' OR parent_sku NOT IN (SELECT parent_sku FROM product_master_data))
    AND COALESCE(NULLIF(parent_sku,''), sku, barcode, id::text) ~ '^[0-9]+$'
)
DELETE FROM pos_products WHERE id IN (SELECT id FROM orphans);

-- 2) Cadastros com apenas 1 variação (tamanho|cor)
WITH single_var AS (
  SELECT parent_sku
  FROM pos_products
  WHERE parent_sku IS NOT NULL AND parent_sku <> ''
  GROUP BY parent_sku
  HAVING COUNT(DISTINCT (COALESCE(size,'') || '|' || COALESCE(color,''))) = 1
)
DELETE FROM pos_products WHERE parent_sku IN (SELECT parent_sku FROM single_var);

